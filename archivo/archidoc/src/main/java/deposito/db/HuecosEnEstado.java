package deposito.db;

public class HuecosEnEstado {
	String estado;
	int numHuecos;

	public HuecosEnEstado() {
	}

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}

	public int getNumHuecos() {
		return numHuecos;
	}

	public void setNumHuecos(int numHuecos) {
		this.numHuecos = numHuecos;
	}
}